//Operators in JavaScript.

//1. Arithmetic Operators
let num1 =100;
let num2 = 50;

//Addition operator
let result = num1 + num2;
console.log(result);

//Multiplication operator
let result1 = num1 * num2;
console.log(result1);

//division operator
let result2 = num1 / num2;
console.log(result2);

//Subtraction operator
let result3 = num1 - num2;
console.log(result3);

//Modulo operator
let result4 = num1 % num2;
console.log(result4);